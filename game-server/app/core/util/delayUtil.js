var DelayUtil = module.exports;

var DELAY_PREFIX = 'delay_';

var START_GAME = '_start_game';
var START_VOTE = '_start_vote';

DelayUtil.getDelayStartGame = function(roomId) {
	return DELAY_PREFIX + roomId + START_GAME;
};

DelayUtil.getDelayEndVote = function(roomId) {
	return DELAY_PREFIX + roomId + START_VOTE;
};